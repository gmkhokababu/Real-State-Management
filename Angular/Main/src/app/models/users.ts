export class Users {
    id:any;
    name:any;
    email:any;
    userName:any;
    password:any;
    role:any;
    constructor(id:any,name:any,email:any,userName:any,password:any,role:any){
        this.id=id;
        this.name=name;
        this.email=email;
        this.userName=userName;
        this.password=password;
        this.role=role;
    }
}
